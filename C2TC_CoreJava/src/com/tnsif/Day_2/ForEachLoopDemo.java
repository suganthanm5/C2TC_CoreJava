package com.tnsif.Day_2;

public class ForEachLoopDemo {

	public static void main(String[] args) {
		int a[] = {10,20,30,40,50};
		for (int i:a) {
			System.out.println(i);
		}
		char ch[] = {'j','a','v','a'};
		for (char n:ch) {
			System.out.print(n);
		}
		System.out.println(" ");
		String S1[]= {"java","programmming","learning"};
		for (String s:S1) {
			System.out.println(s+" ");
		}

		

	}

}
